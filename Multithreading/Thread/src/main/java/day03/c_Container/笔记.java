package day03.c_Container;

public class 笔记 {

    /*
     * ArrayBlockQueue
     *      大小固定
     *
     * PriorityBlockQueue
     *      无界
     *      排序
     *
     * LinkedBlockingQueue
     *      大小为 X 或 Integer.MAX_VALUE
     *
     * SynchronousQueue
     *      没有容量
     *      在存一个时必须取一个，反之亦然
     *
     * DelayQueue
     *      无界
     *      元素过期才能删除，元素要实现 Delayed 接口（元素延迟处理）
     *      内部使用的是 PriorityQueue（优先级队列）
     *
     * LinkedBlockingDeque
     *      大小为 X 或 Integer.MAX_VALUE
     *      双向存取
     *
     * LinkedTransferQueue
     *      无界
     *      可以有担保地交付元素
     *
     */

}
